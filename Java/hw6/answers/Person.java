// This is the Person Class. 
//
// It has a protected member variable of type int, named id.
// It also has an overloaded constructor which initializes the id variable
//*****************************************************************
public abstract class Person {
	
	protected int id;	
	
	//Constructor	
	public Person (int id_new) 
	{
		id = id_new;
	}
 
}